# I型指令前递测试程序（含复杂数据依赖链）
# 初始化寄存器
addi x2, x0,  3       # x2 = 3
addi x3, x0,  7       # x3 = 7
addi x4, x0,  0x0F    # x4 = 0b1111

# 测试场景1：连续I型指令（EX→EX前递）
addi x5, x3, 5        # x5 = 7+5=12 (0xC)
slli x6, x5, 2        # x6 = 12<<2=48 ← 需要前递x5

# 测试场景2：跨指令前递（MEM→EX前递）
andi x7, x6, 0x1F     # x7 = 48 & 31=16
addi x8, x7, -4       # x8 = 16-4=12 ← 需要前递x7

# 测试场景3：混合前递（前递链+立即数）
srli x9, x8, 1        # x9 = 12>>1=6
xori x10, x9, 0xFF    # x10=6^0xFF=0xF9 ← 需要前递x9

# 测试场景4：多路径前递冲突
addi x13, x12, -9     # x13=0x109-9=0x100
srai x14, x13, 4      # x14=0x100>>4=0x10（算术右移）
ori  x15, x14, 0x1    # x15=0x10|1=0x11 ← 需要同时前递x14和立即数
